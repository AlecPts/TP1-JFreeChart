public class Passager {
    String id;
    int classe;
    int age;
    int sexe;
    int survivant;

    public Passager(String id, int classe, int age, int sexe, int survivant) {
        this.id = id;
        this.classe = classe;
        this.age = age;
        this.sexe = sexe;
        this.survivant = survivant;
    }
}
